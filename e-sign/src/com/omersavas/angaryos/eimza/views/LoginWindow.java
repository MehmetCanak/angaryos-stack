    private boolean validation()
    {
        if(jTextField1.getText().length() == 0)
        {
            GeneralHelper.showMessageBox("Mail boş geçilemez!");
            return false;
        }
        
        if(jPasswordField1.getText().length() == 0)
        {
            GeneralHelper.showMessageBox("Şifre boş geçilemez!");
            return false;
        }
        
        return true;
    }
    
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Thread control = new Thread(() ->{
             try {
                jLabel3.setText("Hatırlanan kullanıcı deneniyor...");
                if(GeneralHelper.getSession().controlRememberUser()){
                    dispose();
                }
                else {
                    this.fillRememberedMail();
                    
                    if(SmartOp.getCardTerminals().length == 0) {
                        jLabel3.setText("Takılı cihaz yok!");
                    }
                    else {
                        jLabel3.setText("");
                        jButton1.setEnabled(true);
                    }
                }
            } catch (Exception e) {
                Log.send(e);
            }
             
            jButton1.setEnabled(true);
        });
        control.start();
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if(!validation()) return;

        jButton1.setEnabled(false);

        Thread login = new Thread(() ->{
            try {
                if(GeneralHelper.getSession().loginWithMail(jTextField1.getText(), jPasswordField1.getText()))
                dispose();
            } catch (Exception e) {
                Log.send(e);
            }

            jButton1.setEnabled(true);
        });
        login.start();
    }//GEN-LAST:event_jButton1ActionPerformed

