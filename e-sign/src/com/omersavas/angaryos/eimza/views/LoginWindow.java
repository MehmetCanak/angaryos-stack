    private void fillRememberedMail() throws IOException {
        File f = new File(GeneralHelper.getSession().mailPath);
        if(!f.exists()) return;

        String email = (new String(Files.readAllBytes(Paths.get(GeneralHelper.getSession().mailPath))));
        jTextField1.setText(email);
    }
